# encoding: utf-8

hypo_const = [0x3f, 0x3d,
              0x3e, 0x37, 0x3b, 0x1f, 0x2f,
              0x35, 0x36, 0x39, 0x3a, 0x1d, 0x1e, 0x2d, 0x2e, 0x17, 0x1b,
              0x27, 0x2b, 0x15, 0x16, 0x19, 0x1a, 0x25, 0x26, 0x29, 0x2a]
# 原本用一个六位二进制表示每个假设，每两位就描述一个属性。记录时用十六进制这样比较方便

sample = [0x15, 0x2a]
sum = 0
for hypo in hypo_const:
    if hypo | sample[1] != hypo and hypo | sample[0] == hypo:
        sum += 1
        # python哲学里的一句话：只用一种方式解决问题
        print(bin(hypo))
print(sum)





